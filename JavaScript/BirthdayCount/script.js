var daysLeft = 60;

for(var i = daysLeft; i > 0; i--) {
    if(i >= 30) {
        console.log(i + ' days until my birthday. Such a long time... :(')
    } else if(i > 5 && i < 30) {
        console.log(i + ' days until my birthday. So Close!!!')
    } else {
        console.log(i + ' DAYS UNTIL MY BIRTHDAY!!!!!!!!!')
    }
}

console.log('HAPPY BIRTHDAY TO MEEEEEEEEE!!!!!')